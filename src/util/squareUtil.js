export let setRandomClass = (data, randomClass) => {
    for (var i = 0; i < data.length; i++) {
        for (var j = 0; j < data.length; j++) {
            if (data[i][j] != 0 && data[i][j] != 'none')
                data[i][j] = randomClass
            else
                data[i][j] = 'none'
        }
    }
    return data
}


export let addTime = (timeCount, setTime) => {
    if (timeCount % 5 == 0) {
        let seconds = timeCount / 5
        var showTime;
        if (seconds >= 3600) {
            var tmp = seconds % 3600
            var tmpMinute = Math.floor(tmp / 60)
            showTime = Math.floor(seconds / 3600) + "小时" + tmpMinute + '分' + tmp % 60 + '秒'
        } else if (seconds >= 60) {
            showTime = Math.floor(seconds / 60) + "分" + seconds % 60 + '秒'
        } else {
            showTime = seconds + '秒'
        }
        setTime(showTime)
    }
}


export let getScore = (data) => {
    switch (data) {
        case 0:
            return 0
        case 1:
            return 10
        case 2:
            return 40
        case 3:
            return 60
        default:
            return 80
    }
}
